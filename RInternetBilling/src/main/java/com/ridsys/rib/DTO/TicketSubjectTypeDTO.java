package com.ridsys.rib.DTO;

public class TicketSubjectTypeDTO {

	private int id;
	private String name;

	public TicketSubjectTypeDTO(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
